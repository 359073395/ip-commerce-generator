export function getAgentProfile(moduleId) {
  return agentProfiles[moduleId] || agentProfiles['ip-positioning'];
}

export function formatAgentProfile(profile) {
  return [
    `Role：${profile.role}`,
    `Goal：${profile.goal}`,
    `Tools：${profile.tools.join('、')}`,
    `Rules：${[...profile.rules, ...sharedOutputRules].join('；')}`,
    `Output：${profile.outputFocus.join('、')}`,
  ].join('\n');
}

